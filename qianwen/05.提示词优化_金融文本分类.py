# 导入 OpenAI 客户端，用来调用兼容 OpenAI 协议的大模型接口。
from openai import OpenAI

# 创建客户端对象。
# 这里使用的是阿里云百炼兼容 OpenAI 的接口地址。
# API Key 不在代码里写死，而是默认从环境变量中读取。
client = OpenAI(
    base_url="https://dashscope.aliyuncs.com/compatible-mode/v1",
)

# few-shot 示例数据。
# 字典的 key 是分类名称。
# 字典的 value 是属于该分类的示例文本列表。
# 后面会把这些示例拼进 messages 中，作为“示范样本”告诉模型该怎么分类。
examples_data = {
    "宏观经济": [
        "国家统计局公布数据显示，2025年四季度国内生产总值同比增长5.2%，社会消费品零售总额继续回升。",
        "央行宣布下调金融机构存款准备金率0.25个百分点，以保持流动性合理充裕并支持实体经济发展。",
        "受海外通胀回落和出口修复影响，多家机构上调了对明年经济增速的预测。",
    ],
    "货币政策": [
        "人民银行开展5000亿元中期借贷便利操作，中标利率维持不变。",
        "美联储议息会议决定暂不加息，并表示将继续关注就业和通胀数据变化。",
        "公开市场逆回购持续投放，短端资金利率整体保持平稳。",
    ],
    "股市": [
        "A股三大指数集体收涨，半导体和算力板块领涨，北向资金全天净买入超50亿元。",
        "受业绩超预期带动，该上市公司股价开盘涨停，成交额快速放大。",
        "沪深两市成交额连续第三个交易日突破一万亿元，市场风险偏好明显回升。",
    ],
    "债券": [
        "10年期国债到期收益率下行6个基点，债券市场整体走强。",
        "某城投平台成功发行5年期中期票据，票面利率较上期明显下降。",
        "信用债收益率分化加剧，低评级品种利差继续走阔。",
    ]
}

# 分类标签列表。
# 这里列出模型最终允许输出的类别范围。
# system prompt 里会用到这份列表，限制模型只能从这些类别中选一个。
examples_types = [
    "宏观经济",
    "货币政策",
    "股市",
    "债券"
]

# 待分类文本列表。
# 这里放的是你真正想让模型判断类别的文本。
# 程序会遍历这个列表，逐条发送给模型分类。
questions = [
    "央行今日开展3000亿元7天期逆回购操作，中标利率维持在1.70%不变，市场流动性总体充裕。",
    "某上市券商发布研报称，人形机器人产业链正进入商业化提速阶段，建议关注核心零部件公司。",
    "受国际金价上涨带动，黄金ETF近期份额持续增长，多只相关基金净值创下新高。",
    "国家统计局表示，工业增加值和固定资产投资增速继续回升，经济运行延续恢复态势。",
    "小美喜欢小明"
]

# 初始化对话消息列表。
# 先放一条 system 消息，用来定义模型的角色、分类范围和输出格式要求。
messages = [
    {
        "role": "system",
        "content": (
            # 告诉模型当前任务是金融文本分类。
            "你是金融文本分类专家。"
            # 明确限定模型只能从 examples_types 中选择一个分类。
            f"你只能从以下类别中选择一个作为答案：{', '.join(examples_types)}。"
            # 如果文本内容和这些金融分类无关，就输出“不清楚”。
            "如果无法判断，就回答：不清楚。"
            # 要求模型只输出类别名称，不要额外解释。
            "你的回答只能输出分类名称，不要解释。"
        ),
    }
]

# 把示例数据加工成 few-shot 对话格式。
# 这里的思路是：
# 1. 先给模型一条“待分类文本”
# 2. 再给模型一条“正确分类答案”
# 这样模型就能学习你希望的分类方式和输出格式。
for key, value in examples_data.items():
    # key 是分类名，比如“宏观经济”。
    # value 是该分类下对应的示例文本列表。
    for text in value:
        # 先追加一条 user 消息，模拟“请对这段文本分类”。
        messages.append({"role": "user", "content": f"请对这段金融文本分类：{text}"})
        # 再追加一条 assistant 消息，告诉模型这条示例的标准答案是什么。
        messages.append({"role": "assistant", "content": key})

# 遍历所有待分类文本，逐条调用模型。
for q in questions:
    # 发起一次聊天补全请求。
    response = client.chat.completions.create(
        # 指定使用的模型名称。
        model="qwen3-max",
        # 在已有 few-shot 示例后面，再拼接当前这一条待分类文本。
        messages=messages + [{"role": "user", "content": f"请对这段金融文本分类：{q}"}],
    )
    # 打印原始文本，便于核对分类结果对应的是哪一条输入。
    print(f"文本：{q}")
    # 打印模型返回的分类结果。
    print(f"分类：{response.choices[0].message.content}")
    # 打印分隔线，让多条结果看起来更清楚。
    print("-" * 50)